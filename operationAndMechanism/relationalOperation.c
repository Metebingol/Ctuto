#include<stdio.h>





int main(){
    // The relational arithmetic operation {==,!=,>,<,>=,<=}
    // == equal operation
    int a;
    a = (4==3);
    // != not equal operation
    int b;
    b = (4==3);
    // > bigger operation
    int c;
    c = (4 > 3);
    // < smaller operation
    int d;
    d = (4 < 3);
    // >= equal and bigger 
    int e ;
    e = (4>=3);
    // <= equal and smaller 
    int f;
    f = (4<=3);
    return 0;
}