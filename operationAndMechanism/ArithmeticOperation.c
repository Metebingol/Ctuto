#include<stdio.h>




int main(){

    // The basic Arithmetic operation {+,-,*,/,%,++,--}

    // + summation operation
    int p;
    p = 15 + 20;
    // - subtraction operation
    int i;
    i = 20 -15 ;
    // * multiplication operation
    int n;
    n = 3 * 5;
    // / division operation 
    int a;
    a = 8/2;
    // % modules operator 
    int r;
    r = 10%4;
    // ++ increment operator 
    int t;
    t++;
    // -- increment operator
    int l;
    l--;
    return 0;
}